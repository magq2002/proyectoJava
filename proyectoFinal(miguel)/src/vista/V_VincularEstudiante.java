    private void botonVinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonVinActionPerformed
        Estudiante vincular = new Estudiante();
        Long cedDes = Long.parseLong(this.cedUpdate.getText());
        this.proy = (Proyecto) controlP.readProyecto(Long.parseLong((String) comboBoxP.getSelectedItem()));
        vincular.setCedula(Long.parseLong(this.cedUpdate.getText()));
        vincular.setNombre(this.nomUpdate.getText());
        vincular.setCelular(Long.parseLong(this.celularUpdate.getText()));
        vincular.setEmail(this.emailUpdate.getText());
        vincular.setFechaNacimiento(LocalDate.of(Integer.parseInt(this.añoUpdate.getText()), Integer.parseInt(this.mesUpdate.getText()), Integer.parseInt(this.diaUpdate.getText())));

        if (controlP.vincularEstudiante(proy, cedDes, vincular) == true) {
            JOptionPane.showMessageDialog(this, "ESTUDIANTE VINCULADO", "INFORMACION", JOptionPane.INFORMATION_MESSAGE);

            this.cedUpdate.setText("");
            this.nomUpdate.setText("");
            this.celularUpdate.setText("");
            this.emailUpdate.setText("");
            this.añoUpdate.setText("");
            this.mesUpdate.setText("");
            this.diaUpdate.setText("");

        } else {
            JOptionPane.showMessageDialog(this, "PROCESO NO REALIZADO", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botonVinActionPerformed

