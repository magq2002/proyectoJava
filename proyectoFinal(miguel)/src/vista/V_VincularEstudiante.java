    private void buscarUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarUpdateActionPerformed
        Estudiante estudeUpdate;

        if (!(this.busCedUpdate.getText().equals(""))) {

            if (!(control.readPersona(Long.parseLong(busCedUpdate.getText())) == null)) {

                estudeUpdate = (Estudiante) control.readPersona(Long.parseLong(busCedUpdate.getText()));

                this.cedUpdate.setText("" + estudeUpdate.getCedula());
                this.nomUpdate.setText("" + estudeUpdate.getNombre());
                this.celularUpdate.setText("" + estudeUpdate.getCelular());
                this.emailUpdate.setText("" + estudeUpdate.getEmail());
                this.añoUpdate.setText("" + estudeUpdate.getFechaNacimiento().getYear());
                this.mesUpdate.setText("" + estudeUpdate.getFechaNacimiento().getMonthValue());
                this.diaUpdate.setText("" + estudeUpdate.getFechaNacimiento().getDayOfMonth());

                JOptionPane.showMessageDialog(this, "DATOS ENCONTRADOS ", "INFORMACION", JOptionPane.INFORMATION_MESSAGE);

            } else {
                JOptionPane.showMessageDialog(this, "CEDULA NO ENCONTRADA ", "ERROR", JOptionPane.ERROR_MESSAGE);
                this.cedUpdate.setText("");
                this.nomUpdate.setText("");
                this.celularUpdate.setText("");
                this.emailUpdate.setText("");
                this.añoUpdate.setText("");
                this.mesUpdate.setText("");
                this.diaUpdate.setText("");

            }//cedula no  encontrada

        } else {
            JOptionPane.showMessageDialog(this, "CEDULA MAL DIGITADA", "ERROR", JOptionPane.ERROR_MESSAGE);
            this.cedUpdate.setText("");
            this.nomUpdate.setText("");
            this.celularUpdate.setText("");
            this.emailUpdate.setText("");
            this.añoUpdate.setText("");
            this.mesUpdate.setText("");
            this.diaUpdate.setText("");

        }
    }//GEN-LAST:event_buscarUpdateActionPerformed

    private void botonDesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonDesActionPerformed

        Long cedDes = Long.parseLong(this.cedUpdate.getText());
        this.proy = (Proyecto) controlP.readProyecto(Long.parseLong((String) comboBoxP.getSelectedItem()));
        if (controlP.desvincularEstudiante(proy, cedDes) == true) {
            JOptionPane.showMessageDialog(this, "ESTUDIANTE DESVINCULADO", "INFORMACION", JOptionPane.INFORMATION_MESSAGE);

            this.cedUpdate.setText("");
            this.nomUpdate.setText("");
            this.celularUpdate.setText("");
            this.emailUpdate.setText("");
            this.añoUpdate.setText("");
            this.mesUpdate.setText("");
            this.diaUpdate.setText("");

        } else {
            JOptionPane.showMessageDialog(this, "PROCESO NO REALIZADO", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botonDesActionPerformed

