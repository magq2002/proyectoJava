    private void busReadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_busReadActionPerformed
        LineaInvestigacion lineaRead;
        if (!(this.buscarRead.getText().equals(""))) {
            
            if (!(control.readInformacion(Long.parseLong(buscarRead.getText())) == null)) {
                
                lineaRead = (LineaInvestigacion) control.readInformacion(Long.parseLong(buscarRead.getText()));
                
                this.idRead.setText("" + lineaRead.getId());
                this.nomRead.setText("" + lineaRead.getNombreInv());
                
                JOptionPane.showMessageDialog(this, "DATOS ENCONTRADOS ", "INFORMACION", JOptionPane.INFORMATION_MESSAGE);
                
            }//verificar que el valor sea diferente de nulo
            else {
                JOptionPane.showMessageDialog(this, "ID NO ENCONTRADA ", "ERROR", JOptionPane.ERROR_MESSAGE);
                this.idRead.setText("");
                this.nomRead.setText("");
                
            }//cedula no  encontrada

        } else {
            JOptionPane.showMessageDialog(this, "ID MAL DIGITADA", "ERROR", JOptionPane.ERROR_MESSAGE);
            this.idRead.setText("");
            this.nomRead.setText("");
            
        }
    }//GEN-LAST:event_busReadActionPerformed

