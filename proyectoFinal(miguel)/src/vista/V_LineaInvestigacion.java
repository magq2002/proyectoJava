    private void jBotonCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBotonCrearActionPerformed
        if (!(idCrear.getText().equals("") || nomCrear.getText().equals(""))) {
            LineaInvestigacion a = new LineaInvestigacion(nomCrear.getText(), Long.parseLong(idCrear.getText()));
            if (control.crearInformacion(a)) {
                
                JOptionPane.showMessageDialog(this, "INFORMACIÓN GUARDADA", "INFORMACIÓN", JOptionPane.INFORMATION_MESSAGE);
                this.idCrear.setText("" + control.generarId());
            } else {
                JOptionPane.showMessageDialog(this, "INFORMACIÓN ERRÓNEA", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "INFORMACIÓN ERRÓNEA", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jBotonCrearActionPerformed

