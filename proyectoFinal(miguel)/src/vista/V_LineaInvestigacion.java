    private void busUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_busUpdateActionPerformed
        LineaInvestigacion lI = new LineaInvestigacion();
        
        if (!buscarUpdate.getText().equals("")) {
            lI = (LineaInvestigacion) control.readInformacion(Long.parseLong(buscarUpdate.getText()));
            if (lI != null) {
                JOptionPane.showMessageDialog(this, "ENCONTRADO", "INFORMACIÓN", JOptionPane.INFORMATION_MESSAGE);
                idUpdate.setText("" + lI.getId());
                nomUpdate.setText("" + lI.getNombreInv());
            } else {
                JOptionPane.showMessageDialog(this, "NO HAY REGISTROS", "ERROR", JOptionPane.ERROR_MESSAGE);
                idUpdate.setText("");
                nomUpdate.setText("");
                buscarUpdate.setText("");
            }
        } else {
            JOptionPane.showMessageDialog(this, "INGRESE UNA ID", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_busUpdateActionPerformed

    private void actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_actualizarActionPerformed
        if (!idUpdate.getText().equals("")) {
            if (control.updateInformacion(new LineaInvestigacion(nomUpdate.getText(), Long.parseLong(idUpdate.getText())))) {
                JOptionPane.showMessageDialog(this, "INFORMACIÓN ACTUALIZADA", "INFORMACIÓN", JOptionPane.INFORMATION_MESSAGE);
                
            } else {
                JOptionPane.showMessageDialog(this, "NO SE PUDO ACTUALIZAR", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
            idUpdate.setText("");
            nomUpdate.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "INFORMACIÓN VACÍA", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_actualizarActionPerformed

