    private void GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarActionPerformed
        try {
            if (!(this.cedCreate.getText().equals("") || this.nomCreate.getText().equals("")
                    || this.telCreate.getText().equals("")
                    || this.emailCreate.getText().equals("")
                    || this.diaCreate.getText().equals("")
                    || this.mesCreate.getText().equals("")
                    || this.añoCreate.getText().equals(""))) {

                Estudiante nuevo = new Estudiante();//se crea el objeto nuevo  profesor 
                nuevo.setCedula(Integer.parseInt(this.cedCreate.getText())); //le da una cedula al  objeto

                nuevo.setNombre(this.nomCreate.getText()); //le da un nombre al  objeto 

                nuevo.setCelular(Long.parseLong(this.telCreate.getText())); //le da los apellidos al  objeto

                nuevo.setEmail(this.emailCreate.getText()); //le da un email  al objeto 

                nuevo.setFechaNacimiento(LocalDate.of(Integer.parseInt(this.añoCreate.getText()), Integer.parseInt(this.mesCreate.getText()), Integer.parseInt(this.diaCreate.getText())));

                if (control.createPersona(nuevo)) {
                    JOptionPane.showMessageDialog(this, "DATOS GUARDADOS CORRECTAMENTE", "INFORMACION", JOptionPane.INFORMATION_MESSAGE);
                    this.cedCreate.setText("");
                    this.nomCreate.setText("");
                    this.telCreate.setText("");
                    this.emailCreate.setText("");
                    this.añoCreate.setText("");
                    this.mesCreate.setText("");
                    this.diaCreate.setText("");
                    

                } else {
                    JOptionPane.showMessageDialog(this, "NO SE PUDIERON GUARDAR LOS DATOS", "ERROR", JOptionPane.ERROR_MESSAGE);
                     this.cedCreate.setText("");
                    this.nomCreate.setText("");
                    this.telCreate.setText("");
                    this.emailCreate.setText("");
                    this.añoCreate.setText("");
                    this.mesCreate.setText("");
                    this.diaCreate.setText("");
                }

            } else {
                JOptionPane.showMessageDialog(this, "PORFAVOR DIGITE TODOS LOS CAMPOS ", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR FECHA MAL  DIGITADA \n" + e, "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_GuardarActionPerformed

